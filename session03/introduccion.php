<h2>Introduccion Javascript</h2>

<p>JavaScript (/ˈdʒɑːvəˌskrɪpt/), a menudo abreviado como JS, es un lenguaje de programación que se ajusta a la especificación ECMAScript. JavaScript es de alto nivel, a menudo compilado justo a tiempo, y multiparadigma. Tiene sintaxis de corchetes, tipificación dinámica, orientación a objetos basada en prototipos y funciones de primera clase.</p>

<p>Junto con el HTML y el CSS, JavaScript es una de las tecnologías centrales de la World Wide Web. JavaScript permite páginas web interactivas y es una parte esencial de las aplicaciones web. La gran mayoría de los sitios web lo usan para el comportamiento de la página del lado del cliente, y todos los principales navegadores web tienen un motor de JavaScript dedicado para ejecutarlo.</p>

<p>Como lenguaje multiparadigma, JavaScript soporta estilos de programación impulsados por eventos, funcionales e imperativos. Tiene interfaces de programación de aplicaciones (API) para trabajar con texto, fechas, expresiones regulares, estructuras de datos estándar y el Modelo de Objeto de Documento (DOM). Sin embargo, el lenguaje en sí no incluye ninguna entrada/salida (E/S), como instalaciones de red, almacenamiento o gráficos, ya que el entorno del host (normalmente un navegador web) proporciona esas API.</p>

<p>Los motores de JavaScript se utilizaban originalmente sólo en los navegadores web, pero ahora están incorporados en algunos servidores, normalmente a través de Node.js. También están incorporados en una variedad de aplicaciones creadas con marcos como Electron y Cordova.</p>

<p>Aunque hay similitudes entre JavaScript y Java, incluyendo el nombre del lenguaje, la sintaxis y las respectivas bibliotecas estándar, los dos lenguajes son distintos y difieren mucho en su diseño.</p>

<h4>Historia</h4>

<p><strong>Creacion de Netscape</strong></p>
<p>
El navegador web Mosaico fue lanzado en 1993. Como primer navegador con una interfaz gráfica de usuario accesible a personas no técnicas, desempeñó un papel destacado en el rápido crecimiento de la naciente World Wide Web. Los principales desarrolladores de Mosaic fundaron entonces la corporación Netscape, que lanzó un navegador más perfeccionado, Netscape Navigator, en 1994. Navigator se convirtió rápidamente en el navegador más utilizado.
</p>
<p>
Durante estos años de formación de la web, las páginas web sólo podían ser estáticas, sin capacidad de comportamiento dinámico después de que la página se cargara en el navegador. Había un deseo en la floreciente escena de desarrollo de la web de eliminar esta limitación, así que en 1995, Netscape decidió añadir un lenguaje de scripts al Navigator. Persiguieron dos caminos para lograrlo: colaborar con Sun Microsystems para incrustar el lenguaje de programación Java, mientras que también contrataron a Brendan Eich para incrustar el lenguaje Scheme.
</p>
<p>
La dirección de Netscape pronto decidió que la mejor opción era que Eich idease un nuevo lenguaje, con una sintaxis similar a la de Java y menos parecida a la de Scheme o a la de otros lenguajes de script existentes. Aunque el nuevo lenguaje y su implementación de intérprete se llamaron oficialmente LiveScript cuando se enviaron por primera vez como parte de una versión de Navigator en septiembre de 1995, el nombre se cambió a JavaScript tres meses después.
</p>
<p>
La elección del nombre de JavaScript ha causado confusión, a veces dando la impresión de que es un spin-off de Java. Como Java era el nuevo lenguaje de programación de moda en ese momento, esto se ha caracterizado como una estratagema de marketing de Netscape para dar su propio caché de nuevo lenguaje.
</p>

<p><strong>Alcanzando la madurez</strong></p>
<p>
El ambicioso trabajo sobre el lenguaje continuó durante varios años, culminando en una extensa colección de adiciones y refinamientos que se formalizaron con la publicación de ECMAScript 6 en 2015.
</p>
<p>
De 2016 a 2019, una nueva versión del estándar ECMAScript se publicó cada año, pero el alcance de los cambios fue mucho menor que en la 5ª o 6ª edición. Por lo tanto, JavaScript puede ser considerado ahora como un lenguaje maduro que se ha establecido en gran medida.
</p>
<p>
El ecosistema actual de JavaScript tiene muchas bibliotecas y marcos, prácticas de programación establecidas y un mayor uso de JavaScript fuera de los navegadores web. Además, con el aumento de las aplicaciones de una sola página y otros sitios web con mucho JavaScript, se han creado varios transpiladores para ayudar al proceso de desarrollo.
</p>
